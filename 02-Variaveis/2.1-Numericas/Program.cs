class Programa{

  static void Main(string[] args){
    // Variavel int
    Console.WriteLine("Projeto 2 - Variaveis Numericas");
    int idade=27;
    Console.WriteLine(idade);
    // Variavel double
    double salario=3000.12;
    Console.WriteLine(salario);
    //Conversão
    int salarioInteiro= (int)salario;
    Console.WriteLine(salarioInteiro);
    // Variavel long
    long x=20000000000000000;
    Console.WriteLine(x);
    //Varavel short
    short y=10;
    Console.WriteLine(y);  
    //Variavel float
    float z=1.62f;
    Console.WriteLine(z);
}

}